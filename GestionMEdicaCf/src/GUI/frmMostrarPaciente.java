    private void BuscarPaciente() {
    Conexion conn = new Conexion("gestionhospital");
    DefaultTableModel modelo = (DefaultTableModel) dtMedicoCF.getModel();
    modelo.setRowCount(0);

    Connection con = null;
    PreparedStatement ps = null;
    ResultSet rs = null;

    try {
        con = conn.getConexion();
        String sql = "SELECT id, Nombre, Edad, Direccion, Historial, Enfermedad FROM pacientecf WHERE Nombre LIKE ?";
        ps = con.prepareStatement(sql);
        ps.setString(1, "%" + txtCriterioCF.getText() + "%");
        rs = ps.executeQuery();

        while (rs.next()) {
            Object[] dataPerson = {
                rs.getInt("id"),
                rs.getString("Nombre"),
                rs.getInt("Edad"),
                rs.getString("Direccion"),
                rs.getInt("Historial"),
                rs.getString("Enfermedad")
            };
            modelo.addRow(dataPerson);
        }
    } catch (SQLException e) {
        e.printStackTrace();
    } finally {
        try {
            if (rs != null) rs.close();
            if (ps != null) ps.close();
            if (con != null) con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}

